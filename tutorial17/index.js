var letter = prompt("Enter your letter");

letter = letter.toLowerCase(); // convert to lowercase

switch (letter) {   // switch statement
    case "a":   
        alert("Vowel");
        break;
    case "e":
        alert("Vowel");
        break;
    case "i":
        alert("Vowel");
        break;
    case "o":
        alert("Vowel");
        break;
    case "u":
        alert("Vowel");
        break;
    default:
        alert("Consonant");
        break;
}

document.write("<h1>Switch Statement  </h1>");